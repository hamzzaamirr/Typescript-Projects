let addition = (num1, num2) => {
    return (num1 + num2);
};
let subtraction = (num1, num2) => {
    return (num1 - num2);
};
let division = (num1, num2) => {
    return (num1 / num2);
};
let multiplication = (num1, num2) => {
    return (num1 * num2);
};
let exponential = (num1, num2) => {
    return (num1 ** num2);
};
export { addition, subtraction, division, multiplication, exponential };
