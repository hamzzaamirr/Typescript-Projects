
let addition = (num1:number, num2:number)=>{
    return (num1+num2);
}
let subtraction = (num1:number, num2:number)=>{
    return (num1-num2);
}
let division = (num1:number, num2:number)=>{
    return (num1/num2);
}
let multiplication = (num1:number, num2:number)=>{
   return (num1*num2);
}
let exponential = (num1:number, num2:number)=>{
    return (num1**num2);
}
export {addition,subtraction,division,multiplication,exponential};
